#include <stdio.h>
/**
 * main - prints all single digit numbers of base 10 starting from 0
 * Description: using the main function
 * this program prints "Programming is positive, zero, or negative"
 * Return-: Always 0 (success)
 */
int main(void)
{
char c;

for (c = 0; c <= 0; c++)
{
	printf(c);
}
putchar('\n');
return (0);
}
